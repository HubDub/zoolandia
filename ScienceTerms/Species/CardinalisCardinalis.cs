namespace Zoolandia.Animals
{
    public class CardinalisCardinalis : Species
    {
        public CardinalisCardinalis()
        {
            this.url = "https://en.wikipedia.org/wiki/Northern_cardinal";
            this.scientificName = "Cardinalis Cardinalis";
            this.commonName = "Northern Cardinal";
            this.genus = new Cardinalis();
        }
    }
}